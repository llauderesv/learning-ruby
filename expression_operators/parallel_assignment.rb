# Parallel assignment is any assignment expression that has one more than one left values,
# more that right values, or both.

x, y, z = 1, 2, 3 # x=1, y=2, z=3

puts "The value of x is: #{x}"

puts "The value of y is: #{y}"

puts "The value of z is: #{z}"

# Swapping values

x,y=y,x # same as x=y; y=x